package com.intuit.app.model;

public class BusinessUnit {
	private int bu_id;
	private int bu_name;
	private int bu_reg_date;
	private int bu_remove_date;
	private int org_id;
	public int getBu_id() {
		return bu_id;
	}
	public void setBu_id(int bu_id) {
		this.bu_id = bu_id;
	}
	public int getBu_name() {
		return bu_name;
	}
	public void setBu_name(int bu_name) {
		this.bu_name = bu_name;
	}
	public int getBu_reg_date() {
		return bu_reg_date;
	}
	public void setBu_reg_date(int bu_reg_date) {
		this.bu_reg_date = bu_reg_date;
	}
	public int getBu_remove_date() {
		return bu_remove_date;
	}
	public void setBu_remove_date(int bu_remove_date) {
		this.bu_remove_date = bu_remove_date;
	}
	public int getOrg_id() {
		return org_id;
	}
	public void setOrg_id(int org_id) {
		this.org_id = org_id;
	}
	

}
